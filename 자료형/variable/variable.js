// 변수(Variable) : 값을 저장할 때 사용하는 식별자이다. 이름은 변수이지만 숫자 자료형뿐만 아니라 모든 자료형을 저장할 수 있다. 

/*
var: 전역변수, 재선언 가능
let: 지역변수, 재선언 불가능
*/

/*
변수를 사용하기 위한 단계
1. 변수를 선언
2. 변수에 값을 할당
*/

// 변수를 선언
var pi;

// 변수에 값을 할당 (변수를 선언하고 처음으로 값을 할당하는 것을 '변수를 초기화한다'라고 한다.)
pi = 3.141592;

// 변수를 선언하고 초기화 (일반적으로 변수선언과 할당을 함께 해준다.)
var pi = 3.141592;